"""Localized prompt variants and a centralized resolver.

Evolve V2 supports two content languages: English and French (Canadian).
This module wraps the existing English prompt templates in
``app.prompts.templates`` and adds parallel French-Canadian variants so the
rest of the application can request prompts via ``get_prompts(language)``
without having to know which strings change between languages.

Design:
- The English prompt strings are imported as-is (single source of truth).
- French strings live next to them here so reviewers can diff easily.
- A small ``LocalizedPrompts`` dataclass groups the prompts the agents
  actually need at runtime; ``get_prompts(language)`` returns one.
- Ingestion (skillset) prompts are exposed via ``get_ingestion_prompts``.
"""

